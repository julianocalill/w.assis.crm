-- 1. Tabela de Corretores/Usuários (Conectada ao Auth do Supabase)
CREATE TABLE IF NOT EXISTS corretores (
    id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    nome_completo TEXT,
    email TEXT UNIQUE,
    avatar_url TEXT,
    perfil TEXT DEFAULT 'Vendedor', -- Pode ser 'Vendedor' ou 'Gerente'
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 2. Tabelas de Configurações Gerais
CREATE TABLE IF NOT EXISTS ramos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome TEXT NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS seguradoras (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome TEXT NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS origens (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome TEXT NOT NULL UNIQUE
);

-- 3. Tabela de Funis e Etapas
CREATE TABLE IF NOT EXISTS funis (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome TEXT NOT NULL,
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

CREATE TABLE IF NOT EXISTS etapas_funil (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    funil_id BIGINT REFERENCES funis(id) ON DELETE CASCADE,
    nome TEXT NOT NULL,
    cor TEXT DEFAULT 'bg-blue-500',
    ordem INTEGER NOT NULL,
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 4. Tabela de Contatos (Clientes)
CREATE TABLE IF NOT EXISTS contatos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    corretor_id UUID REFERENCES corretores(id),
    nome TEXT NOT NULL,
    documento TEXT,
    telefone TEXT,
    email TEXT,
    chatwoot_id TEXT,
    is_segurado BOOLEAN DEFAULT FALSE,
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 5. Tabela de Oportunidades (Negócios/Cards)
CREATE TABLE IF NOT EXISTS oportunidades (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    funil_id BIGINT REFERENCES funis(id),
    contato_id BIGINT REFERENCES contatos(id) ON DELETE CASCADE,
    responsavel_id BIGINT REFERENCES contatos(id),
    corretor_id UUID REFERENCES corretores(id),
    gerente_id UUID REFERENCES corretores(id),
    nome_segurado TEXT NOT NULL,
    vigencia DATE,
    retorno DATE,
    ativo BOOLEAN DEFAULT TRUE,
    origem TEXT,
    seguradora TEXT,
    -- Modelo 1:1 (um seguro por oportunidade)
    ramo_seguro TEXT,
    tipo_seguro TEXT,
    premio_liquido NUMERIC DEFAULT 0,
    comissao_percentual NUMERIC DEFAULT 0,
    agenciamento_percentual NUMERIC DEFAULT 0,
    indicador TEXT,
    etapa_nome TEXT NOT NULL,
    status TEXT DEFAULT 'aberto', -- 'aberto', 'ganho', 'perdido'
    motivo_perda TEXT,
    data_fechamento TIMESTAMP WITH TIME ZONE,
    excluido BOOLEAN DEFAULT FALSE,
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

