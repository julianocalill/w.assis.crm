-- 1. Tabela de Corretores/Usuários (Conectada ao Auth do Supabase)
CREATE TABLE IF NOT EXISTS corretores (
    id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    nome_completo TEXT,
    email TEXT UNIQUE,
    avatar_url TEXT,
    perfil TEXT DEFAULT 'Vendedor', -- Pode ser 'Vendedor' ou 'Gerente'
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 2. Tabelas de Configurações Gerais
