-- 4. Tabela de Contatos (Clientes)
CREATE TABLE IF NOT EXISTS contatos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    corretor_id UUID REFERENCES corretores(id),
    nome TEXT NOT NULL,
    documento TEXT,
    telefone TEXT,
    email TEXT,
    chatwoot_id TEXT,
    is_segurado BOOLEAN DEFAULT FALSE,
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 5. Tabela de Oportunidades (Negócios/Cards)
CREATE TABLE IF NOT EXISTS oportunidades (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    funil_id BIGINT REFERENCES funis(id),
    contato_id BIGINT REFERENCES contatos(id) ON DELETE CASCADE,
    responsavel_id BIGINT REFERENCES contatos(id),
    corretor_id UUID REFERENCES corretores(id),
    gerente_id UUID REFERENCES corretores(id),
    nome_segurado TEXT NOT NULL,
    vigencia DATE,
    retorno DATE,
    ativo BOOLEAN DEFAULT TRUE,
    origem TEXT,
    seguradora TEXT,
    -- Modelo 1:1 (um seguro por oportunidade)
    ramo_seguro TEXT,
    tipo_seguro TEXT,
    premio_liquido NUMERIC DEFAULT 0,
    comissao_percentual NUMERIC DEFAULT 0,
    agenciamento_percentual NUMERIC DEFAULT 0,
    indicador TEXT,
    etapa_nome TEXT NOT NULL,
    status TEXT DEFAULT 'aberto', -- 'aberto', 'ganho', 'perdido'
    motivo_perda TEXT,
    data_fechamento TIMESTAMP WITH TIME ZONE,
    excluido BOOLEAN DEFAULT FALSE,
    is_checked BOOLEAN NOT NULL DEFAULT FALSE,
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

