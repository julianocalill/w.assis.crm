-- ==============================================================================
-- RLS — Gerente vs Vendedor (corretores.perfil = 'Gerente' vê todas as oportunidades)
-- Funis / etapas_funil / cadastros auxiliares: RLS não habilitada aqui; cliente só com JWT autenticado.
