-- 6. Tabela de Histórico (Timeline)
CREATE TABLE IF NOT EXISTS historico_oportunidades (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    oportunidade_id BIGINT REFERENCES oportunidades(id) ON DELETE CASCADE,
    criado_por UUID REFERENCES corretores(id),
    tipo TEXT NOT NULL,
    descricao TEXT NOT NULL,
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 7. Tabela de Anexos (Arquivos)
CREATE TABLE IF NOT EXISTS anexos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    oportunidade_id BIGINT REFERENCES oportunidades(id) ON DELETE CASCADE,
    criado_por UUID REFERENCES corretores(id),
    nome_arquivo TEXT NOT NULL,
    url_arquivo TEXT NOT NULL,
    tamanho_bytes BIGINT,
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

